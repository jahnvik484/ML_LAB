def train(x,y):
  for i ,val in enumerate(y):
    if val=='yes':
      h=x[i].copy()
      break

  for i,val in enumerate(x):
    if y[i]=='yes':
      for z in range(len(h)):
        if val[z] != h[z]:
          h[z]='?'
        else:
          pass
  return h

